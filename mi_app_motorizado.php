        function openPicture(){
            //Inside of the event listener callback function
            if (!("documentPictureInPicture" in window)) {
              console.log("PIP mode not supported");
              return;
            }
            openPIP();
        }
        
        const openPIP = async() => {
            let pipContent = document.querySelector("#versiones");
            let options = {
              width: 300,
              height: 100,
              disallowReturnToOpener: true
            };
            let pipWindow = await documentPictureInPicture.requestWindow(options);
            
            //Copiar Styles
            [...document.styleSheets].forEach((styleSheet) => {
            try {
                  const link = document.createElement('link');
            
                  link.rel = 'stylesheet';
                  link.type = styleSheet.type;
                  link.media = styleSheet.media;
                  link.href = styleSheet.href;
                  pipWindow.document.head.appendChild(link);
                } catch (e) {
                  console.log("No se pudo copiar los estilos");
                }
            });
            pipWindow.document.body.append(pipContent);
        }
    </script>

